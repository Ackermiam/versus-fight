export const DoubleDamage = (damage: number) => {
  return damage * 2;
};

export const DoubleHeal = (heal: number) => {
  return heal * 2;
};

export const DivideDamage = (damageEnemy: number) => {
  return damageEnemy / 2;
};

export const DivineHeal = (heal: number) => {
  return heal + 20;
};

export const DivineDamage = (damage: number) => {
  return damage + 20;
};

export const QuickHeal = (heal: number) => {
  return heal + 10;
};

